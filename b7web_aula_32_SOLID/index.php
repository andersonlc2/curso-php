S - SINGLE RESPONSIBILITY PRINCIPE (Princípio da reponsabilidade única)
    * Toda classe deve ter apenas uma responsábilidade.
    * Melhora: Alto acoplamento, difícil entendimento do seu objetivo


O - OPEN-CLOSED PRINCIPE (Princípio Aberto-Fechado)
    * Devem estar abertas para extenção mas fechadas para modificação.
    * Uso de interfaces e aplicando o polimorfismo


L - LISKOV SUBSTITUTION PRINCIPE (Princípio da substituição de Liskov)
    * Liskov -> aprensentou esse príncipio
    * Uma classe que extende um outra classe deve funcionar no mesmo local onde a classe mãe 
          funciona, desde que o retorno permaneça do mesmo tipo

          
I - INTERFACE SEGREGATION PRINCIPLE (Princípio da segregação da Interface)
    * Uma interface deve ter apenas os métodos essenciais e que vão ser utilizados pela interface
            que esta implementando eles.

D - DEPENDENCY INVERSION PRINCIPLE (Princípio da inversão da dependência)
    
